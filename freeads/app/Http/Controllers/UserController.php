     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $user = auth()->user();
        $password = Hash::make($user->password);
        $this->validate($request,[
            'name' => 'required',
            'email' => 'required',
            'password' => '',
        ]);
        // $user = User::find($id);
        $user->update($request->all());
        // var_dump($request);
        return redirect()->route('profile.index');
    }

